from src.preprocess_data import parse_changes
import pytest


@pytest.mark.parametrize("changes,expected", [
    ("2", ["2"]),
    ("4", ["4"]),
    ("5", ["5"]),
    ("6", ["6"]),
    ("7", ["7"]),
    ("9", ["9"]),
    ("11", ["11"]),
    ("13", ["13"]),
    ("+2", ["+2"]),
    ("94", ["9", "4"]),
    ("+4", ["+4"]),
    ("+9", ["+9"]),
    ("4+2", ["4", "+2"]),
    ("42", ["4", "2"]),
    ("#2", ["#2"]),
    ("b9", ["b9"]),
    ("b64", ["b6", "4"]),
    ("+b2", ["+b2"]),
    ("4#2", ["4", "#2"]),
    ("974", ["9", "7", "4"]),
    ("9+74", ["9", "+7", "4"]),
    ("#72", ["#7", "2"]),
    ("64+2", ["6", "4", "+2"]),
    ("+4+2", ["+4", "+2"]),
    ("#4#2", ["#4", "#2"]),
    ("9#74", ["9", "#7", "4"]),
    ("+#4+2", ["+#4", "+2"]),
    ("+6+#4", ["+6", "+#4"]),
    ("+#74+2", ["+#7", "4", "+2"]),
    ("7642", ["7", "6", "4", "2"]),
    ("11#7b6", ["11", "#7", "b6"]),
])
def test_parse_changes(changes, expected):
    assert parse_changes(changes) == expected
